def _normalize(text: str) -> str:
    """Lowercase + strip punctuation for comparison."""
    text = text.lower()
    text = re.sub(r"[।,!?؟،:;\"'()\[\]{}]", " ", text)
    return text


def _score(message_norm: str, keywords: list[str]) -> int:
    """Return how many keywords appear in the normalised message."""
    count = 0
    for kw in keywords:
        if kw.lower() in message_norm:
            count += 1
    return count


class OfflineChatService:
    """
    Lightweight, zero-network advisory engine.
    Matches the caller's message against a curated Bangla KB and returns a
    ChatResponse that is schema-identical to the Gemini-backed online route.
    """

    # The minimum score (keyword hits) required to consider a match.
    _THRESHOLD = 1

    _FALLBACK = ChatResponse(
        text=(
            "আপনার প্রশ্নের জন্য ধন্যবাদ। এই মুহূর্তে অফলাইন মোডে আপনার প্রশ্নের সঠিক উত্তর দেওয়া সম্ভব হচ্ছে না। "
            "অনুগ্রহ করে নিচের যেকোনো একটি উপায়ে সাহায্য নিন:\n\n"
            "- **ইন্টারনেট সংযোগ চালু থাকলে** অনলাইন মোডে ফিরে আসুন।\n"
            "- **স্থানীয় উপজেলা কৃষি কর্মকর্তা (UAO)**-র সাথে সরাসরি যোগাযোগ করুন।\n"
            "- **কৃষি তথ্য সার্ভিস (AIS) হেল্পলাইন**: ১৬১২৩ নম্বরে কল করুন।\n\n"
            "সাধারণ প্রশ্নগুলি (ধানের ব্লাস্ট, সার মাত্রা, সেচ, খরা, বন্যা, ইত্যাদি) অফলাইনেও সমাধান করা যায়।"
        )
    )

    def get_response(self, message: str) -> ChatResponse:
        if not message or not message.strip():
            return self._FALLBACK

        norm = _normalize(message)

        best_score = 0
        best_entry: Optional[dict] = None

        for entry in _KB:
            score = _score(norm, entry["keywords"])
            if score > best_score:
                best_score = score
                best_entry = entry

        if best_score < self._THRESHOLD or best_entry is None:
            return self._FALLBACK

        diag = None
        if best_entry.get("diagnosis"):
            raw = best_entry["diagnosis"]
            diag = Diagnosis(
                title=raw["title"],
                description=raw["description"],
                steps=[
                    TreatmentStep(
                        label=s["label"],
                        action=s["action"],
                        status=s.get("status"),
                    )
                    for s in raw["steps"]
                ],
            )

        return ChatResponse(text=best_entry["response"], diagnosis=diag)


offline_chat_service = OfflineChatService()
